package test1;

public class Number {

	int a = 10;

	void isNumberPositive() {
		if (a > 0) {
			System.out.println("Number is positive");
		}
	}

	void isNumberZero() {
		if (a == 0) {
			System.out.println("Number is Zero");
		}
	}

	void isNumberNegative() {
		if (a < 0) {
			System.out.println("Number is negative");
		}
	}

}
